def _draw_report_body(c: canvas.Canvas, w, h, title: str, category: str, rows: list[list[str]], summary_lines: list[str]):
    """Shared helper for all report PDFs."""
    y = h - 110

    # Report title
    c.setFont("Helvetica-Bold", 16)
    c.setFillColor(PRIMARY_COLOR)
    c.drawString(40, y, title.upper())
    y -= 8
    c.setStrokeColor(ACCENT_COLOR)
    c.setLineWidth(2)
    c.line(40, y, 40 + len(title) * 9, y)
    y -= 25

    # Category & date
    c.setFont("Helvetica", 10)
    c.setFillColor(TEXT_SECONDARY)
    c.drawString(40, y, f"Category: {category}  |  Generated: {datetime.now().strftime('%B %d, %Y at %I:%M %p')}")
    y -= 15
    c.drawString(40, y, f"Generated By: Administrator Console  |  Format: PDF")
    y -= 30

    # Executive summary
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(PRIMARY_COLOR)
    c.drawString(40, y, "EXECUTIVE SUMMARY")
    y -= 18

    c.setFont("Helvetica", 9)
    c.setFillColor(black)
    for line in summary_lines:
        c.drawString(40, y, line)
        y -= 14
    y -= 15

    # Data table
    c.setFont("Helvetica-Bold", 11)
    c.setFillColor(PRIMARY_COLOR)
    c.drawString(40, y, "DETAILED BREAKDOWN")
    y -= 20

    col_count = len(rows[0]) if rows else 3
    col_width = (w - 80) / col_count
    t = Table(rows, colWidths=[col_width] * col_count)
    t.setStyle(TableStyle([
        # Header row
        ('BACKGROUND', (0, 0), (-1, 0), PRIMARY_COLOR),
        ('TEXTCOLOR', (0, 0), (-1, 0), white),
        ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
        ('FONTSIZE', (0, 0), (-1, 0), 9),
        # Body rows
        ('FONTNAME', (0, 1), (-1, -1), 'Helvetica'),
        ('FONTSIZE', (0, 1), (-1, -1), 9),
        ('TEXTCOLOR', (0, 1), (-1, -1), black),
        ('ROWBACKGROUNDS', (0, 1), (-1, -1), [white, LIGHT_BG]),
        # Grid
        ('GRID', (0, 0), (-1, -1), 0.5, BORDER_COLOR),
        ('TOPPADDING', (0, 0), (-1, -1), 7),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 7),
        ('LEFTPADDING', (0, 0), (-1, -1), 8),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
    ]))

    tw, th = t.wrap(0, 0)
    if y - th < 80:
        # If table is too tall, just draw what fits
        t.drawOn(c, 40, max(80, y - th))
    else:
        t.drawOn(c, 40, y - th)
    y = y - th - 30

    # Notes
    if y > 120:
        c.setFont("Helvetica-Bold", 9)
        c.setFillColor(PRIMARY_COLOR)
        c.drawString(40, y, "NOTES & DISCLAIMERS")
        y -= 16
        c.setFont("Helvetica-Oblique", 8)
        c.setFillColor(TEXT_SECONDARY)
        c.drawString(40, y, "• Data is compiled from the Pinesphere SIMP enterprise database and may be subject to real-time variance.")
        y -= 12
        c.drawString(40, y, "• All financial figures are in INR and exclude applicable taxes unless stated otherwise.")
        y -= 12
        c.drawString(40, y, "• This report is auto-generated and does not constitute audited financial advice.")


