def _draw_header(c: canvas.Canvas, width, height):
    """Draw branded header on every page."""
    c.saveState()
    # Header bar background
    c.setFillColor(PRIMARY_COLOR)
    c.rect(0, height - 72, width, 72, fill=1, stroke=0)

    # Brand name
    c.setFillColor(white)
    c.setFont("Helvetica-Bold", 18)
    c.drawString(40, height - 35, BRAND_NAME.upper())

    # Tagline
    c.setFont("Helvetica", 8)
    c.setFillColor(HexColor("#94a3b8"))
    c.drawString(40, height - 50, BRAND_TAGLINE)

    # Right side: date
    c.setFillColor(HexColor("#cbd5e1"))
    c.setFont("Helvetica", 8)
    c.drawRightString(width - 40, height - 35, datetime.now().strftime("%B %d, %Y"))
    c.drawRightString(width - 40, height - 50, f"Ref: PS/{datetime.now().strftime('%Y%m%d')}/{random.randint(1000, 9999)}")

    # Thin accent line under header
    c.setStrokeColor(ACCENT_COLOR)
    c.setLineWidth(2)
    c.line(0, height - 72, width, height - 72)

    c.restoreState()


def _draw_footer(c: canvas.Canvas, width, height):
    """Draw branded footer on every page."""
    c.saveState()

    # Footer line
    c.setStrokeColor(BORDER_COLOR)
    c.setLineWidth(0.5)
    c.line(40, 55, width - 40, 55)

    # Footer text
    c.setFont("Helvetica", 7)
    c.setFillColor(TEXT_SECONDARY)
    c.drawString(40, 42, f"{BRAND_NAME} • {BRAND_ADDRESS}")
    c.drawString(40, 32, f"Email: {BRAND_EMAIL}  |  Phone: {BRAND_PHONE}  |  Web: {BRAND_WEBSITE}")

    # Page number
    c.setFont("Helvetica-Bold", 7)
    c.setFillColor(ACCENT_COLOR)
    c.drawRightString(width - 40, 42, f"Page {c.getPageNumber()}")

    # Confidentiality notice
    c.setFont("Helvetica-Oblique", 6)
    c.setFillColor(TEXT_SECONDARY)
    c.drawCentredString(width / 2, 20, "This document is auto-generated by Pinesphere SIMP and is confidential. Unauthorized distribution is prohibited.")

    c.restoreState()


def _build_pdf(draw_content_fn, title: str = "Document") -> bytes:
    """Scaffold a PDF with header/footer, call draw_content_fn for body."""
    buf = io.BytesIO()
    width, height = A4

    c = canvas.Canvas(buf, pagesize=A4)
    c.setTitle(title)
    c.setAuthor(BRAND_NAME)

    _draw_header(c, width, height)
    _draw_footer(c, width, height)
    draw_content_fn(c, width, height)
    c.save()

    buf.seek(0)
    return buf.read()


